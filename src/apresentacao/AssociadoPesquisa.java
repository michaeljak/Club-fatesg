    private void tblResultadoMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblResultadoMousePressed
        try {
            int linha = tblResultado.getSelectedRow();
            String codigoSelecionado = tblResultado.getValueAt(linha, 0).toString();
            int codigo = Integer.parseInt(codigoSelecionado);

            if(cadastro.isVisible())
                cadastro.setVisible(false);
                
            
            cadastro = new AssociadoCadastro(codigo);
            this.pnlPrincipal.add(cadastro);
            cadastro.setVisible(true);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_tblResultadoMousePressed


