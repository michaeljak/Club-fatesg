    private void carregarTabela(EAssociado parametro) throws SQLException {
        Vector<String> cabecalho = new Vector<String>();
        cabecalho.add("CÃ³digo");
        cabecalho.add("Nome");
        cabecalho.add("Tipo Associado");

        Vector detalhe = new Vector();

        for (EAssociado item : new NAssociado().listar(parametro.getNome())) {
            Vector<String> linha = new Vector<String>();
            linha.add(item.getCodigo() + "");
            linha.add(item.getNome());
            linha.add(item.getTipoAssociado().getDescricao());
            detalhe.add(linha);
        }

        tblResultado.setModel(new DefaultTableModel(detalhe, cabecalho));
    }

    private void pesquisar() throws SQLException {
        EAssociado parametro = new EAssociado();

        if (!txtParametro.getText().isEmpty()) {
            parametro.setNome(txtParametro.getText());
        }

        carregarTabela(parametro);
    }
