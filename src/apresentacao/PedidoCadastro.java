    public void limparTela() {

        pedido = new EPedido();

        txtCodigo.setText("");
        txtQuantidade.setText("");

        cmdExcluir.setEnabled(false);

        //Carrega novamente ss combos
        carregarCombos();

        //Carrega a tabela de itens
        carregarTabela(pedido.getListaItens());

    }

    private void carregarCombos() {

        try {

            //Carregando produtos
            cmbProduto.removeAll();
            cmbProduto.addItem(new util.ComboItem("Selecione...", "0"));

            for (EProduto produto : new NProduto().listar(new EProduto())) {
                cmbProduto.addItem(new util.ComboItem(produto.getNome(),
                        String.valueOf(produto.getCodigo())));
            }

            //Carregando associados
            cmbAssociado.removeAll();
            cmbAssociado.addItem(new util.ComboItem("Selecione...", "0"));

            for (EAssociado associado : new NAssociado().listar("")) {
                cmbAssociado.addItem(new util.ComboItem(associado.getNome(),
                        String.valueOf(associado.getCodigo())));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this.principal, e.getMessage());
        }

    }

    public void setPosicao() {
        Dimension d = this.getDesktopPane().getSize();
        this.setLocation((d.width - this.getSize().width) / 2,
                (d.height - this.getSize().height) / 2);
    }

