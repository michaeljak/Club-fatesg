    private void carregarTabela(List<EItemPedido> itens) {

        Vector<String> cabecalho = new Vector<String>();
        cabecalho.add("Código");
        cabecalho.add("Produto");
        cabecalho.add("Quantidade");
        cabecalho.add("Valor Unitário");
        cabecalho.add("Valor Total");

        Vector detalhe = new Vector();

        pedido.setValorTotal(0);
        try {
            for (EItemPedido item : itens) {

                Vector<String> linha = new Vector<String>();
                linha.add(item.getProduto().getCodigo() + "");
                linha.add(item.getProduto().getNome());
                linha.add(item.getQuantidade() + "");
                linha.add(NumberFormat.getCurrencyInstance().format(item.getProduto().getValor()));
                linha.add(NumberFormat.getCurrencyInstance().format(item.getQuantidade() * item.getProduto().getValor()));
                detalhe.add(linha);

                pedido.setValorTotalAcumulando(item.getQuantidade() * item.getProduto().getValor());
            }
            tblItem.setModel(new DefaultTableModel(detalhe, cabecalho));

            txtValorTotal.setText(NumberFormat.getCurrencyInstance().format(pedido.getValorTotal()));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
        }

    }


