    private void adicionaItemNaLista(EItemPedido item) {

        boolean achou = false;
        double qtde = 0;
        
        for (EItemPedido itemLista : pedido.getListaItens()) {

            if (itemLista.getProduto().getCodigo() == item.getProduto().getCodigo()) {
                achou = true;
                qtde = itemLista.getQuantidade();
                itemLista.setQuantidade(qtde + item.getQuantidade());
            }

        }
        if (!achou) {
            pedido.getListaItens().add(item);
        }

    }
