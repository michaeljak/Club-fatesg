    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        try {

            EItemPedido item = new EItemPedido();

            util.ComboItem itemSelecionado = (util.ComboItem) cmbProduto.getModel().getSelectedItem();

            if (itemSelecionado.getCodigo().equals("0")) {
                throw new Exception("É necessário selecionar um produto.");
            }

            int codigoProduto = Integer.parseInt(itemSelecionado.getCodigo());

            EProduto produto;
            produto = new NProduto().consultar(codigoProduto);
            item.setProduto(produto);

            if (txtQuantidade.getText() == null
                    || txtQuantidade.getText().trim().equals("")
                    || txtQuantidade.getText().equals("0")) {
                throw new Exception("É necessário informar a quantidade do "
                        + "produto.");
            }

            item.setQuantidade(Double.parseDouble(txtQuantidade.getText()));

            if (item.getQuantidade() <= 0) {
                throw new Exception("É necessário informar a quantidade do "
                        + "produto.");
            }

            adicionaItemNaLista(item);

            carregarTabela(pedido.getListaItens());

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "É necessário informar uma quantidade válida.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }

    }//GEN-LAST:event_btnAdicionarActionPerformed


    private void btnRetirarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRetirarActionPerformed
        try {

            if (tblItem.getSelectedRow() == -1) {
                throw new Exception("É necessário selecionar um item na lista.");
            }

            if (pedido.getListaItens().isEmpty()) {
                throw new Exception("É necessário ter algum item na lista para ser removido.");
            }

            pedido.getListaItens().remove(tblItem.getSelectedRow());

            carregarTabela(pedido.getListaItens());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnRetirarActionPerformed

