    private void cmdSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdSalvarActionPerformed
        try {

            util.ComboItem itemSelecionado = (util.ComboItem) cmbAssociado.getModel().getSelectedItem();

            if (itemSelecionado.getCodigo().equals("0")) {
                throw new Exception("É necessário selecionar um associado.");
            }

            int codigoAssociado = Integer.parseInt(itemSelecionado.getCodigo());

            EAssociado associado;
            associado = new NAssociado().consultar(codigoAssociado);
            pedido.setAssociado(associado);

            NPedido nPedido = new NPedido();
            nPedido.salvar(pedido);

            JOptionPane.showMessageDialog(null, "Pedido registrado com sucesso!");
            limparTela();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "É necessário informar uma quantidade válida.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_cmdSalvarActionPerformed

